package ubicrypt.core.util;

import com.fasterxml.jackson.core.JsonGenerator;
import com.fasterxml.jackson.databind.SerializerProvider;
import com.fasterxml.jackson.databind.ser.std.StdSerializer;

import java.io.IOException;

public class PGPKValueSerializer extends StdSerializer<PGPKValue> {
    public PGPKValueSerializer(final Class<PGPKValue> t) {
        super(t);
    }


    @Override
    public void serialize(final PGPKValue value, final JsonGenerator gen, final SerializerProvider provider) throws IOException {
        gen.writeBinary(value.getValue().getEncoded());
    }
}
